    end
end

function Levels.changeOffset(dir, v)
	if dir == "up" then
		Levels.map_offset_y = Levels.map_offset_y + v
	elseif dir == "down" then
		Levels.map_offset_y = Levels.map_offset_y - v
	end
	if dir == "left" then
		Levels.map_offset_x = Levels.map_offset_x + v
	elseif dir == "right" then
		Levels.map_offset_x = Levels.map_offset_x - v
	end
end

function Levels.update()
	if Levels.map_offset_x > - Levels.tile_w and Levels.map_x > 0 then
	   Levels.map_offset_x = Levels.map_offset_x - Levels.tile_w
	   Levels.map_x = Levels.map_x - 1
	elseif Levels.map_offset_x < - (Levels.tile_w * 2) and Levels.map_x < Levels.map_w - Levels.map_display_w then
	   Levels.map_offset_x = Levels.map_offset_x + Levels.tile_w
	   Levels.map_x = Levels.map_x + 1
	end
	if Levels.map_offset_y > - Levels.tile_h and Levels.map_y > 0 then
	   Levels.map_offset_y = Levels.map_offset_y - Levels.tile_h
	   Levels.map_y = Levels.map_y - 1
	elseif Levels.map_offset_y < - (Levels.tile_h * 2) and Levels.map_y < Levels.map_h - Levels.map_display_h then
	   Levels.map_offset_y = Levels.map_offset_y + Levels.tile_h
	   Levels.map_y = Levels.map_y + 1
	end
end
