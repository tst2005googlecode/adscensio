function love.update( dt )
    -- get input
    if love.keyboard.isDown( "down" ) then
        --map_offset_y = map_offset_y - move_speed * dt
		if hero.y < 400 then
		   hero.y = hero.y + hero.dt * dt
		else
		   map_offset_y = map_offset_y - hero.dt * dt
		end
    end
    if love.keyboard.isDown( "up" ) then
        --map_offset_y = map_offset_y + move_speed * dt
		if hero.y > 150 then
		   hero.y = hero.y - hero.dt * dt
		else
		   map_offset_y = map_offset_y + hero.dt * dt
		end
    end
    if love.keyboard.isDown( "right" ) then
        --map_offset_x = map_offset_x - move_speed * dt
		if hero.x < 600 then
		   hero.x = hero.x + hero.dt * dt
		else
	       map_offset_x = map_offset_x - hero.dt * dt
		end
    end
    if love.keyboard.isDown( "left" ) then
        --map_offset_x = map_offset_x + move_speed * dt
		if hero.x > 150 then
		   hero.x = hero.x - hero.dt * dt
		else
		   map_offset_x = map_offset_x + hero.dt * dt
		end
    end
	if map_offset_x > -40 and map_x > 0 then
	   map_offset_x = map_offset_x - 40
	   map_x = map_x - 1
	elseif map_offset_x < -80 and map_x < map_w - map_display_w then
	   map_offset_x = map_offset_x + 40
	   map_x = map_x + 1
	end
	if map_offset_y > -40 and map_y > 0 then
	   map_offset_y = map_offset_y - 40
	   map_y = map_y - 1
	elseif map_offset_y < -80 and map_y < map_h - map_display_h then
